package session4;

public class DaysInAWeek {
    public static void main(String[] args) {
        int day = 3; //between 1 to 3 ----true

        if(day ==1)
        {
            System.out.println("Monday");
        }
        else if (day == 2)
        {
            System.out.println("Wednesday");
        }
        else if (day == 3)
        {
            System.out.println("Friday");
        }
        else
        {
            System.out.println("HFM");
        }
    }
}
