package session22;

import java.util.HashSet;

public class HashSetSample {
    public static void main(String[] args) {

        HashSet hs = new HashSet();
        hs.add(10);
        hs.add(20);
        hs.add(30);
        hs.add(40);
        hs.add(50);
        System.out.println(hs); //[50, 20, 40, 10, 30]

    }
}
