package session5;

public class ForLoopDemo {
    public static void main(String [] args) {
        /*
        -> For loop
        syntax:
        for (initialization; condition; increment/decrement)
        {
	    statement or code
        }
         */
        for (int i =1; i<=10; i++) {
            System.out.println(i++);} //2, 4, 6, 8, 10
    }
}
